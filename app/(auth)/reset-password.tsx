/**
 * Reset Password Screen - Mobile App
 * 
 * This screen handles password reset after user clicks the
 * recovery link from their email. The session is already
 * established by auth-callback.tsx before redirecting here.
 */
import React, { useState, useEffect } from 'react';
import {
  View,
  Text,
  TextInput,
  TouchableOpacity,
  StyleSheet,
  Alert,
  ActivityIndicator,
  KeyboardAvoidingView,
  Platform,
  ScrollView,
} from 'react-native';
import { Stack, router } from 'expo-router';
import { SafeAreaView } from 'react-native-safe-area-context';
import { Ionicons } from '@expo/vector-icons';
import { useTheme } from '@/contexts/ThemeContext';
import { assertSupabase } from '@/lib/supabase';
import { LinearGradient } from 'expo-linear-gradient';
import { GlassCard } from '@/components/marketing/GlassCard';
import { GradientButton } from '@/components/marketing/GradientButton';
import { marketingTokens } from '@/components/marketing/tokens';

export default function ResetPasswordScreen() {
  const { theme } = useTheme();
  const [password, setPassword] = useState('');
  const [confirmPassword, setConfirmPassword] = useState('');
  const [showPassword, setShowPassword] = useState(false);
  const [showConfirmPassword, setShowConfirmPassword] = useState(false);
  const [loading, setLoading] = useState(false);
  const [validSession, setValidSession] = useState<boolean | null>(null);
  const [userEmail, setUserEmail] = useState<string | null>(null);

  // Check if user has a valid recovery session
  useEffect(() => {
    const checkSession = async () => {
      try {
        const supabase = assertSupabase();
        const { data: { session }, error } = await supabase.auth.getSession();

        if (error) {
          console.error('[ResetPassword] Session error:', error);
          setValidSession(false);
          return;
        }

        if (session && session.user) {
          setValidSession(true);
          setUserEmail(session.user.email || null);
        } else {
          setValidSession(false);
        }
      } catch (e) {
        console.error('[ResetPassword] Error checking session:', e);
        setValidSession(false);
      }
    };

    checkSession();
  }, []);

  const handleResetPassword = async () => {
    // Validate passwords
    if (!password || !confirmPassword) {
      Alert.alert('Error', 'Please fill in all fields');
      return;
    }

    if (password !== confirmPassword) {
      Alert.alert('Error', 'Passwords do not match');
      return;
    }

    if (password.length < 8) {
      Alert.alert('Error', 'Password must be at least 8 characters long');
      return;
    }

    // Check password strength (basic requirements)
    const hasUpperCase = /[A-Z]/.test(password);
    const hasLowerCase = /[a-z]/.test(password);
    const hasNumber = /[0-9]/.test(password);

    if (!hasUpperCase || !hasLowerCase || !hasNumber) {
      Alert.alert(
        'Weak Password',
        'Password must contain at least one uppercase letter, one lowercase letter, and one number'
      );
      return;
    }

    setLoading(true);
    try {
      const supabase = assertSupabase();
      const { error } = await supabase.auth.updateUser({
        password: password,
      });

      if (error) {
        throw error;
      }

      Alert.alert(
        'Password Updated',
        'Your password has been successfully updated. You can now sign in with your new password.',
        [
          {
            text: 'Sign In',
            onPress: () => router.replace('/(auth)/sign-in'),
          },
        ]
      );
    } catch (e: any) {
      console.error('[ResetPassword] Error:', e);
      Alert.alert('Error', e?.message || 'Failed to update password. Please try again.');
    } finally {
      setLoading(false);
    }
  };

  const handleGoBack = () => {
    router.replace('/(auth)/sign-in');
  };

  // Loading state while checking session
  if (validSession === null) {
    return (
      <LinearGradient
        colors={marketingTokens.colors.gradients.darkBase}
        style={styles.container}
      >
        <SafeAreaView style={styles.safeArea}>
          <View style={styles.loadingContainer}>
            <ActivityIndicator size="large" color={marketingTokens.colors.cyan} />
            <Text style={styles.loadingText}>Verifying your session...</Text>
          </View>
        </SafeAreaView>
      </LinearGradient>
    );
  }

  // Invalid session - redirect to forgot password
  if (validSession === false) {
    return (
      <LinearGradient
        colors={marketingTokens.colors.gradients.darkBase}
        style={styles.container}
      >
        <SafeAreaView style={styles.safeArea}>
          <Stack.Screen
            options={{
              title: 'Reset Password',
              headerShown: false,
            }}
          />
          <View style={styles.errorContainer}>
            <View style={styles.errorIconContainer}>
              <Ionicons name="alert-circle-outline" size={64} color="#EF4444" />
            </View>
            <Text style={styles.errorTitle}>Invalid or Expired Link</Text>
            <Text style={styles.errorText}>
              This password reset link has expired or is invalid.
              Please request a new password reset.
            </Text>
            <TouchableOpacity
              style={styles.errorButton}
              onPress={() => router.replace('/(auth)/forgot-password')}
            >
              <Text style={styles.errorButtonText}>Request New Reset</Text>
            </TouchableOpacity>
            <TouchableOpacity
              style={styles.backLink}
              onPress={handleGoBack}
            >
              <Text style={styles.backLinkText}>Back to Sign In</Text>
            </TouchableOpacity>
          </View>
        </SafeAreaView>
      </LinearGradient>
    );
  }

  // Valid session - show password reset form
  return (
    <LinearGradient
      colors={marketingTokens.colors.gradients.darkBase}
      style={styles.container}
    >
      <SafeAreaView style={styles.safeArea}>
        <Stack.Screen
          options={{
            title: 'Reset Password',
            headerShown: false,
          }}
        />
        <KeyboardAvoidingView
          behavior={Platform.OS === 'ios' ? 'padding' : 'height'}
          style={styles.keyboardView}
        >
          <ScrollView
            contentContainerStyle={styles.scrollContent}
            keyboardShouldPersistTaps="handled"
            showsVerticalScrollIndicator={false}
          >
            {/* Header */}
            <View style={styles.header}>
              <TouchableOpacity
                style={styles.backButton}
                onPress={handleGoBack}
              >
                <Ionicons name="arrow-back" size={24} color="#fff" />
              </TouchableOpacity>
            </View>

            {/* Form Card */}
            <GlassCard style={styles.card}>
              <View style={styles.iconContainer}>
                <LinearGradient
                  colors={[marketingTokens.colors.cyan, marketingTokens.colors.purple]}
                  start={{ x: 0, y: 0 }}
                  end={{ x: 1, y: 1 }}
                  style={styles.iconGradient}
                >
                  <Ionicons name="lock-closed" size={32} color="#fff" />
                </LinearGradient>
              </View>

              <Text style={styles.title}>Create New Password</Text>
              
              {userEmail && (
                <Text style={styles.subtitle}>
                  Setting password for: {userEmail}
                </Text>
              )}

              <Text style={styles.instructions}>
                Please enter your new password. Make sure it's at least 8 characters
                long and includes uppercase, lowercase, and numbers.
              </Text>

              {/* New Password */}
              <View style={styles.inputContainer}>
                <Text style={styles.label}>New Password</Text>
                <View style={styles.inputWrapper}>
                  <Ionicons
                    name="lock-closed-outline"
                    size={20}
                    color="rgba(255,255,255,0.5)"
                    style={styles.inputIcon}
                  />
                  <TextInput
                    style={styles.input}
                    placeholder="Enter new password"
                    placeholderTextColor="rgba(255,255,255,0.3)"
                    secureTextEntry={!showPassword}
                    value={password}
                    onChangeText={setPassword}
                    autoCapitalize="none"
                    autoCorrect={false}
                    returnKeyType="next"
                  />
                  <TouchableOpacity
                    onPress={() => setShowPassword(!showPassword)}
                    style={styles.eyeButton}
                  >
                    <Ionicons
                      name={showPassword ? 'eye-off' : 'eye'}
                      size={20}
                      color="rgba(255,255,255,0.5)"
                    />
                  </TouchableOpacity>
                </View>
              </View>

              {/* Confirm Password */}
              <View style={styles.inputContainer}>
                <Text style={styles.label}>Confirm Password</Text>
                <View style={styles.inputWrapper}>
                  <Ionicons
                    name="lock-closed-outline"
                    size={20}
                    color="rgba(255,255,255,0.5)"
                    style={styles.inputIcon}
                  />
                  <TextInput
                    style={styles.input}
                    placeholder="Confirm new password"
                    placeholderTextColor="rgba(255,255,255,0.3)"
                    secureTextEntry={!showConfirmPassword}
                    value={confirmPassword}
                    onChangeText={setConfirmPassword}
                    autoCapitalize="none"
                    autoCorrect={false}
                    returnKeyType="done"
                    onSubmitEditing={handleResetPassword}
                  />
                  <TouchableOpacity
                    onPress={() => setShowConfirmPassword(!showConfirmPassword)}
                    style={styles.eyeButton}
                  >
                    <Ionicons
                      name={showConfirmPassword ? 'eye-off' : 'eye'}
                      size={20}
                      color="rgba(255,255,255,0.5)"
                    />
                  </TouchableOpacity>
                </View>
              </View>

              {/* Password requirements */}
              <View style={styles.requirements}>
                <PasswordRequirement met={password.length >= 8} text="At least 8 characters" />
                <PasswordRequirement met={/[A-Z]/.test(password)} text="One uppercase letter" />
                <PasswordRequirement met={/[a-z]/.test(password)} text="One lowercase letter" />
                <PasswordRequirement met={/[0-9]/.test(password)} text="One number" />
                <PasswordRequirement
                  met={password.length > 0 && password === confirmPassword}
                  text="Passwords match"
                />
              </View>

              {/* Submit Button */}
              <GradientButton
                onPress={handleResetPassword}
                disabled={loading}
                style={styles.submitButton}
              >
                {loading ? (
                  <ActivityIndicator color="#fff" />
                ) : (
                  <Text style={styles.submitButtonText}>Update Password</Text>
                )}
              </GradientButton>
            </GlassCard>
          </ScrollView>
        </KeyboardAvoidingView>
      </SafeAreaView>
    </LinearGradient>
  );
}

